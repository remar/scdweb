<h2>Välkommen!</h2>

<p>Hej och välkommen till denna sida om Specific Carbohydrate Diet (SCD) på
svenska!</p>

<p> Här kommer jag att samla allmän information om SCD, och andra kostråd
för de som har tarmsjukdomar. Jag är ingen läkare, och ger inte direkt
   medicinska råd, så alla förändringar av din kost sker på egen risk. Det bör
dock noteras att SCD är allmänt hälsosam och balanserad kost. Avbryt aldrig
medicinering på eget bevåg!
</p>

<p>
Den största poängen med den här sidan hoppas jag kommer bli samlingen av recept
för SCD på svenska. Dieten är nog så krånglig att komma igång med, men när alla
recept är på engelska så blir det förbaskat jobbigt! Jag kan inte garantera att
alla recept är korrekta eller är strikt SCD, men jag ska försöka.
</p>

<p>
Lycka till med dieten!
</p>

